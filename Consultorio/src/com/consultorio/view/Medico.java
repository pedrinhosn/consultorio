package com.consultorio.view;

public class Medico {

	String crm;
	String nomeMdc;
	String sexoMdc;
	String emailMdc;
	String estadoMdc;
	String especialidade;
	int idadeMdc;
	int cepMdc;
	int telefoneMdc;
	
	public String getCrm() {
		return crm;
	}
	public void setCrm(String crm) {
		this.crm = crm;
	}
	public String getNomeMdc() {
		return nomeMdc;
	}
	public void setNomeMdc(String nomeMdc) {
		this.nomeMdc = nomeMdc;
	}
	public String getSexoMdc() {
		return sexoMdc;
	}
	public void setSexoMdc(String sexoMdc) {
		this.sexoMdc = sexoMdc;
	}
	public String getEmailMdc() {
		return emailMdc;
	}
	public void setEmailMdc(String emailMdc) {
		this.emailMdc = emailMdc;
	}
	public String getEstadoMdc() {
		return estadoMdc;
	}
	public void setEstadoMdc(String estadoMdc) {
		this.estadoMdc = estadoMdc;
	}
	public String getEspecialidade() {
		return especialidade;
	}
	public void setEspecialidade(String especialidade) {
		this.especialidade = especialidade;
	}
	public int getIdadeMdc() {
		return idadeMdc;
	}
	public void setIdadeMdc(int idadeMdc) {
		this.idadeMdc = idadeMdc;
	}
	public int getCepMdc() {
		return cepMdc;
	}
	public void setCepMdc(int cepMdc) {
		this.cepMdc = cepMdc;
	}
	public int getTelefoneMdc() {
		return telefoneMdc;
	}
	public void setTelefoneMdc(int telefoneMdc) {
		this.telefoneMdc = telefoneMdc;
	}
	
	




}
